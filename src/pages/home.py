import streamlit as st

def show_home_page():
    """显示主页内容"""
    st.markdown("""
    # 欢迎来到初等数学知识库 📐
    
    这是一个交互式的数学学习平台，我们将通过可视化和互动的方式来探索数学概念。
    
    ## 主要内容
    
    ### 🔷 几何
    - 多边形探索
    - 圆与圆周率
    - 特殊图形构造
    
    ### 🔶 代数
    - 基础代数概念
    - 方程与不等式
    - 函数与图像
    
    ### 🔺 算术
    - 数系与运算
    - 比例与百分比
    - 数列与级数
    
    ## 开始探索
    
    在左侧边栏选择一个主题，开始你的数学探索之旅！
    """)
    
    # 添加侧边栏导航
    st.sidebar.markdown("## 导航")
    page = st.sidebar.selectbox(
        "选择一个主题",
        ["首页", "几何/多边形", "几何/圆", "代数/基础", "算术/数系"]
    )
    
    return page